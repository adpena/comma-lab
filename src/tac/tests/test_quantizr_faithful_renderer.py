def test_film_block_modulates_proportional_to_cond() -> None:
    """Smoke: FiLMSepResBlock output magnitude scales when cond is large."""
    block = FiLMSepResBlock(ch=8, cond_dim=4, depth_mult=1).eval()
    x = torch.randn(2, 8, 16, 16)
    cond_zero = torch.zeros(2, 4)
    cond_big = torch.randn(2, 4) * 10.0
    with torch.no_grad():
        y_zero = block(x, cond_zero)
        y_big = block(x, cond_big)
    # Both should differ — even cond_zero produces a non-zero film_proj.bias
    # contribution. The point is they aren't IDENTICAL.
    assert (y_zero - y_big).abs().max().item() > 0.0


